import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const currentDir = dirname(fileURLToPath(import.meta.url));
const dbDir = join(currentDir, "..", "src", "db");

const schemaFiles = [
  ["LAUNCH_CORE_SCHEMA_SQL", "001_launch_core.sql"],
  ["PHASE2_INTEGRITY_SQL", "002_phase2_integrity.sql"],
  ["PHASE3_ALIGNMENT_SQL", "003_phase3_alignment.sql"],
  ["PHASE6_AUTH_SQL", "004_phase6_auth.sql"],
  ["PHASE7_EXTERNAL_OAUTH_SQL", "005_phase7_external_oauth.sql"],
  ["PHASE8_ADMIN_SUITE_SQL", "006_admin_suite.sql"],
  ["PHASE9_EPISTEMIC_CORE_SQL", "007_epistemic_core.sql"],
  ["PHASE10_TOPIC_FORMATS_SQL", "008_topic_formats.sql"],
  ["PHASE11_ADAPTIVE_SCORING_SQL", "009_adaptive_scoring.sql"],
  ["PHASE12_PLATFORM_ANALYTICS_SQL", "010_platform_analytics.sql"],
  ["PHASE13_TOPIC_VIEW_REPUTATION_HISTORY_VOTE_TIMING_SQL", "011_topic_view_reputation_history_vote_timing.sql"],
  ["PHASE14_TOPIC_MEMBER_DROP_TRACKING_SQL", "012_topic_member_drop_tracking.sql"],
  ["PHASE15_TOPIC_CANDIDATES_SQL", "013_topic_candidates.sql"],
  ["PHASE16_ACCOUNT_CLASSES_TOPIC_SOURCES_SQL", "014_account_classes_topic_sources.sql"],
  ["PHASE17_STANCE_AND_VERDICTS_SQL", "015_stance_and_verdicts.sql"],
  ["PHASE18_BEHAVIORAL_AND_TRUST_SQL", "016_behavioral_and_trust.sql"],
  ["PHASE19_ROUND_INSTRUCTION_OVERRIDES_SQL", "017_round_instruction_overrides.sql"],
  ["PHASE20_VOTE_CATEGORIES_SQL", "018_vote_categories.sql"],
];

const output = [
  "// Generated by packages/api/scripts/generate-schema-module.mjs",
  "// Do not edit manually; edit the source SQL files and regenerate.",
  "",
  ...schemaFiles.map(([exportName, fileName]) => {
    const sql = readFileSync(join(dbDir, fileName), "utf8");
    return `export const ${exportName} = ${JSON.stringify(sql)};`;
  }),
  "",
].join("\n");

writeFileSync(join(dbDir, "schema.generated.ts"), output);
